  /// Mevcut kullanƒ±cƒ±yƒ± local storage'dan alƒ±r
  Future<User?> getCurrentUser() async {
    try {
      final prefs = await SharedPreferences.getInstance();
      final userDataString = prefs.getString(AppConstants.userDataKey);

      if (userDataString != null) {
        final userJson = json.decode(userDataString);
        return User.fromJson(userJson);
      }

      return null;
    } catch (e) {
      print('‚ùå Get Current User Error: $e');
      return null;
    }
  }

  /// Kullanƒ±cƒ± verisini local storage'a kaydeder
  Future<bool> saveCurrentUser(User user) async {
    try {
      final prefs = await SharedPreferences.getInstance();
      final userJson = json.encode(user.toJson());
      await prefs.setString(AppConstants.userDataKey, userJson);
      return true;
    } catch (e) {
      print('‚ùå Save Current User Error: $e');
      return false;
    }
  }

  /// Kullanƒ±cƒ± verisini local storage'dan siler
  Future<bool> clearCurrentUser() async {
    try {
      final prefs = await SharedPreferences.getInstance();
      await prefs.remove(AppConstants.userDataKey);
      return true;
    } catch (e) {
      print('‚ùå Clear Current User Error: $e');
      return false;
    }
  }

