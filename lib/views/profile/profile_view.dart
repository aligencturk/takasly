  void _navigateToEmailVerification() async {
    final userViewModel = Provider.of<UserViewModel>(context, listen: false);
    final authViewModel = Provider.of<AuthViewModel>(context, listen: false);
    
    // √ñnce UserViewModel'den user'ƒ± al
    User? user = userViewModel.currentUser;
    
    // Eƒüer UserViewModel'de user yoksa AuthViewModel'den al
    if (user == null) {
      Logger.warning('‚ö†Ô∏è ProfileView: User not found in UserViewModel, trying AuthViewModel...');
      user = authViewModel.currentUser;
    }
    
    if (user == null) {
      Logger.error('‚ùå ProfileView: User is null in both ViewModels, cannot proceed with email verification');
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(
          content: Text('Kullanƒ±cƒ± bilgileri bulunamadƒ±. L√ºtfen tekrar giri≈ü yapƒ±n.'),
          backgroundColor: Colors.red,
          behavior: SnackBarBehavior.floating,
        ),
      );
      return;
    }
    
    Logger.info('üìß ProfileView: Starting email verification for user: ${user.email}');
    Logger.debug('üìß ProfileView: User details - ID: ${user.id}, Name: ${user.name}, Email: ${user.email}');
    Logger.debug('üìß ProfileView: User token: ${user.token?.substring(0, 10)}...');
    Logger.debug('üìß ProfileView: User token length: ${user.token?.length}');
    Logger.debug('üìß ProfileView: User token is null: ${user.token == null}');
    Logger.debug('üìß ProfileView: User token is empty: ${user.token?.isEmpty}');
    
    // Token validation
    if (user.token == null || user.token!.trim().isEmpty) {
      Logger.error('‚ùå ProfileView: User token is empty');
      
      // Token'ƒ± UserService'den almaya √ßalƒ±≈ü
      final userService = UserService();
      final tokenFromService = await userService.getUserToken();
      Logger.debug('üìß ProfileView: Token from UserService: ${tokenFromService?.substring(0, 10)}...');
      
      if (tokenFromService != null && tokenFromService.isNotEmpty) {
        Logger.info('üìß ProfileView: Using token from UserService');
        // UserService'den alƒ±nan token ile devam et
        await _sendEmailVerificationWithToken(tokenFromService);
        return;
      }
      
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(
          content: Text('Kullanƒ±cƒ± token\'ƒ± bulunamadƒ±. L√ºtfen tekrar giri≈ü yapƒ±n.'),
          backgroundColor: Colors.red,
          behavior: SnackBarBehavior.floating,
        ),
      );
      return;
    }

    // Loading dialog g√∂ster
    showDialog(
      context: context,
      barrierDismissible: false,
      builder: (loadingContext) => AlertDialog(
        shape: RoundedRectangleBorder(
          borderRadius: BorderRadius.circular(12),
        ),
        content: Row(
          children: [
            const CircularProgressIndicator(),
            const SizedBox(width: 16),
            const Text('E-posta g√∂nderiliyor...'),
          ],
        ),
      ),
    );

    try {
      print('üìß ProfileView: Sending email verification code with token');
      
      // √ñnce e-posta doƒürulama kodunu g√∂nder (userToken ile)
      final response = await authViewModel.resendEmailVerificationCodeWithToken(
        userToken: user.token ?? '',
      );
      
      Logger.debug('üìß ProfileView: Email verification response received');
      Logger.debug('üìß ProfileView: Response is null: ${response == null}');
      if (response != null) {
        Logger.debug('üìß ProfileView: Response keys: ${response.keys.toList()}');
        Logger.debug('üìß ProfileView: Response contains codeToken: ${response.containsKey('codeToken')}');
      }

      // Loading dialog'u kapat
      if (mounted) Navigator.pop(context);

      if (response != null) {
        // Ba≈üarƒ±lƒ± mesajƒ± g√∂ster
        if (mounted) {
          ScaffoldMessenger.of(context).showSnackBar(
            const SnackBar(
              content: Text('Doƒürulama kodu e-posta adresinize g√∂nderildi'),
              backgroundColor: Colors.green,
              behavior: SnackBarBehavior.floating,
            ),
          );
        }

        // API'den gelen codeToken'ƒ± al veya ge√ßici deƒüer kullan
        String codeToken = 'temp_code_token';
        if (response.containsKey('codeToken') && response['codeToken'] != null) {
          codeToken = response['codeToken'].toString();
        }

        // E-posta doƒürulama sayfasƒ±na y√∂nlendir
        if (mounted) {
          Navigator.pushNamed(
            context,
            '/email-verification',
            arguments: {
              'email': user.email,
              'codeToken': codeToken,
            },
          );
        }
      } else {
        // Token hatasƒ± varsa login sayfasƒ±na y√∂nlendir
        if (authViewModel.errorMessage?.contains('oturum') == true || 
            authViewModel.errorMessage?.contains('token') == true ||
            authViewModel.errorMessage?.contains('giri≈ü') == true) {
          if (mounted) {
            ScaffoldMessenger.of(context).showSnackBar(
              const SnackBar(
                content: Text('Oturum s√ºreniz dolmu≈ü. L√ºtfen tekrar giri≈ü yapƒ±n.'),
                backgroundColor: Colors.red,
                behavior: SnackBarBehavior.floating,
              ),
            );
            
            // Login sayfasƒ±na y√∂nlendir
            Navigator.pushNamedAndRemoveUntil(
              context,
              '/login',
              (route) => false,
            );
          }
        } else {
          // Diƒüer hatalar i√ßin e-posta doƒürulama sayfasƒ±na y√∂nlendir
          if (mounted) {
            ScaffoldMessenger.of(context).showSnackBar(
              SnackBar(
                content: Text(authViewModel.errorMessage ?? 'E-posta g√∂nderilemedi'),
                backgroundColor: Colors.orange,
                behavior: SnackBarBehavior.floating,
              ),
            );
            
            // E-posta doƒürulama sayfasƒ±na y√∂nlendir (hata olsa bile)
            Navigator.pushNamed(
              context,
              '/email-verification',
              arguments: {
                'email': user.email,
                'codeToken': 'temp_code_token',
              },
            );
          }
        }
      }
    } catch (e) {
      // Loading dialog'u kapat
      if (mounted) Navigator.pop(context);

      // Hata mesajƒ± g√∂ster ve e-posta doƒürulama sayfasƒ±na y√∂nlendir
